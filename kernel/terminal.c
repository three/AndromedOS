// Bitmap for storing which key is pressed
static char pressed[16] = { 0 };

static char waitforchar()
{
    uint8_t res, press, bpos, bbyte, bbit;
    while (1) {
        res = keymap[inb(0x60)];
        bpos = (res&0x78)>>3;
        bbyte = pressed[bpos];
        bbit = (bbyte>>(0x07&res))&0x01;
        press = res>>7;
        if (bbit != press) {
            bbyte ^= 0x01<<(0x07&res);
            pressed[bpos] = bbyte;
            if (press)
                return res&0x7F;
        }
    }
}

char terminal_boxread(terminal_box *box)
{
    while (1) {
        char w = waitforchar();
        terminal_boxwritechar(box,w+64);
    }
}
